validMask = isfinite(zMat) & isfinite(framePhaseMat);
blockPhaseResidMat(validMask) = angle(zMat(validMask)) - framePhaseMat(validMask);
blockPhaseResidMat(validMask) = mod(blockPhaseResidMat(validMask) + pi, 2 * pi) - pi;
end


