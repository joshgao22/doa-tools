truthStateDiag.isEnabled = true;
truthStateDiag.refFrameIdx = refFrameIdx;
truthStateDiag.localDoaEval = truthEval.localDoaArrUsed;
truthStateDiag.fdLocalEval = truthEval.fdLocalRaw;
truthStateDiag.aliasBypass = true;
