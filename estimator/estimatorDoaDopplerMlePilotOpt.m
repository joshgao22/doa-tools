end


function ref = localParseReferenceState(scene, satPosEci, satVelEci)
%LOCALPARSEREFERENCESTATE Parse reference state consistently with getLinkParam.

numSat = size(satPosEci, 2);

if ~isfield(scene, 'ref') || isempty(scene.ref)
  refWeight = ones(numSat, 1) / numSat;

  ref = struct();
  ref.type = 'centroid';
  ref.weight = refWeight;
  ref.posEci = satPosEci * refWeight;
  ref.velEci = satVelEci * refWeight;
  return;
end

refIn = scene.ref;
if ~isstruct(refIn)
  error('estimatorDoaDopplerMlePilotOpt:InvalidReferenceType', ...
    'scene.ref must be a struct.');
end
if ~isfield(refIn, 'posEci') || ~isfield(refIn, 'velEci')
  error('estimatorDoaDopplerMlePilotOpt:MissingReferenceState', ...
    'scene.ref.posEci and scene.ref.velEci are required.');
end
if ~isnumeric(refIn.posEci) || ~isequal(size(refIn.posEci), [3, 1])
  error('estimatorDoaDopplerMlePilotOpt:InvalidReferencePosSize', ...
    'scene.ref.posEci must have size 3x1.');
end
if ~isnumeric(refIn.velEci) || ~isequal(size(refIn.velEci), [3, 1])
  error('estimatorDoaDopplerMlePilotOpt:InvalidReferenceVelSize', ...
    'scene.ref.velEci must have size 3x1.');
end

ref = struct();
if isfield(refIn, 'type') && ~isempty(refIn.type)
  ref.type = char(refIn.type);
else
  ref.type = 'custom';
end
if isfield(refIn, 'weight') && ~isempty(refIn.weight)
  ref.weight = refIn.weight;
else
  ref.weight = [];
end
ref.posEci = refIn.posEci;
ref.velEci = refIn.velEci;
end


function sceneUtc = localParseSceneUtc(scene, doaGrid)
%LOCALPARSESCENEUTC Resolve the scene epoch as a 1x6 UTC vector.

sceneUtc = [];
if isfield(scene, 'utc') && ~isempty(scene.utc)
  sceneUtc = localNormalizeUtc(scene.utc);
  return;
end

if iscell(doaGrid)
  baseGrid = doaGrid{1};
elseif isstruct(doaGrid)
  baseGrid = doaGrid;
else
  baseGrid = struct();
end

if isfield(baseGrid, 'utc') && ~isempty(baseGrid.utc)
  sceneUtc = localNormalizeUtc(baseGrid.utc);
end
end


function utcVec = localNormalizeUtc(utcIn)
%LOCALNORMALIZEUTC Normalize utc to a 1x6 numeric date vector.

if isa(utcIn, 'datetime')
  utcVec = datevec(utcIn);
elseif isnumeric(utcIn) && isequal(size(utcIn), [1, 6])
  utcVec = utcIn;
elseif isnumeric(utcIn) && size(utcIn, 2) == 6 && size(utcIn, 1) >= 1
  utcVec = utcIn(1, :);
else
  error('estimatorDoaDopplerMlePilotOpt:InvalidUtc', ...
    'utc must be a datetime scalar, 1x6 date vector, or N-by-6 date matrix.');
end
end


