function localCheckSceneSeq(sceneSeq)
%LOCALCHECKSCENESEQ Check the basic validity of sceneSeq.

requiredField = {'numFrame', 'numSat', 'timeOffsetSec'};
for iField = 1:numel(requiredField)
  if ~isfield(sceneSeq, requiredField{iField})
    error('crbPilotMfDoaDoppler:MissingSceneSeqField', ...
      'sceneSeq.%s is required.', requiredField{iField});
  end
end

if ~isscalar(sceneSeq.numFrame) || sceneSeq.numFrame < 1 || mod(sceneSeq.numFrame, 1) ~= 0
  error('crbPilotMfDoaDoppler:InvalidNumFrame', ...
    'sceneSeq.numFrame must be a positive integer scalar.');
end
if ~isscalar(sceneSeq.numSat) || sceneSeq.numSat < 1 || mod(sceneSeq.numSat, 1) ~= 0
  error('crbPilotMfDoaDoppler:InvalidNumSat', ...
    'sceneSeq.numSat must be a positive integer scalar.');
end
if numel(sceneSeq.timeOffsetSec) ~= sceneSeq.numFrame
  error('crbPilotMfDoaDoppler:InvalidTimeOffset', ...
    'sceneSeq.timeOffsetSec must contain one value per frame.');
end

end


function arrayCell = localParseSceneArray(sceneSeq, numSat)
%LOCALPARSESCENEARRAY Parse array geometry from sceneSeq.

if isfield(sceneSeq, 'array') && ~isempty(sceneSeq.array)
  arrayInput = sceneSeq.array;
elseif isfield(sceneSeq, 'sceneCell') && ~isempty(sceneSeq.sceneCell)
  refIdx = localGetRefFrameIdx(sceneSeq);
  refScene = sceneSeq.sceneCell{refIdx};
  if ~isfield(refScene, 'array') || isempty(refScene.array)
    error('crbPilotMfDoaDoppler:MissingArrayField', ...
      'sceneSeq.array or sceneSeq.sceneCell{refFrameIdx}.array is required.');
  end
  arrayInput = refScene.array;
else
  error('crbPilotMfDoaDoppler:MissingArrayField', ...
    'sceneSeq.array or sceneSeq.sceneCell is required.');
end

if iscell(arrayInput)
  arrayCell = reshape(arrayInput, 1, []);
elseif isstruct(arrayInput)
  if isscalar(arrayInput)
    arrayCell = {arrayInput};
  else
    arrayCell = num2cell(reshape(arrayInput, 1, []));
  end
else
  error('crbPilotMfDoaDoppler:InvalidArrayField', ...
    'sceneSeq.array must be a struct, struct array, or cell array.');
end

if numel(arrayCell) ~= numSat
  error('crbPilotMfDoaDoppler:ArrayCountMismatch', ...
    'The number of arrays must match sceneSeq.numSat.');
end

for iSat = 1:numSat
  if ~isstruct(arrayCell{iSat}) || ~isfield(arrayCell{iSat}, 'positions')
    error('crbPilotMfDoaDoppler:InvalidArray', ...
      'Each scene array entry must be a valid array struct.');
  end
end

end


function [numElement, rotMatCell, satPosEci, satVelEci, refPosEci, refVelEci, utcVec] = ...
  localParseSceneKinematics(sceneSeq, numSat)
%LOCALPARSESCENEKINEMATICS Parse dynamic rotations and ECI kinematics.

numFrame = sceneSeq.numFrame;
numElement = zeros(numSat, 1);
if isfield(sceneSeq, 'array') && ~isempty(sceneSeq.array)
  arrayCell = localParseSceneArray(sceneSeq, numSat);
else
  refIdx = localGetRefFrameIdx(sceneSeq);
  arrayCell = localParseSceneArray(struct('array', sceneSeq.sceneCell{refIdx}.array, ...
    'numSat', numSat), numSat);
end
for iSat = 1:numSat
  numElement(iSat) = size(arrayCell{iSat}.positions, 2);
end

if isfield(sceneSeq, 'rotMat') && ~iscell(sceneSeq.rotMat)
  error('crbPilotMfDoaDoppler:InvalidRotMat', ...
    'sceneSeq.rotMat must be an Nf x Ns cell array when provided.');
end

if isfield(sceneSeq, 'rotMat') && ~isempty(sceneSeq.rotMat) && ...
    isfield(sceneSeq, 'satPosEci') && ~isempty(sceneSeq.satPosEci) && ...
    isfield(sceneSeq, 'satVelEci') && ~isempty(sceneSeq.satVelEci) && ...
    isfield(sceneSeq, 'refPosEci') && ~isempty(sceneSeq.refPosEci) && ...
    isfield(sceneSeq, 'refVelEci') && ~isempty(sceneSeq.refVelEci)
  rotMatCell = sceneSeq.rotMat;
  satPosEci = sceneSeq.satPosEci;
  satVelEci = sceneSeq.satVelEci;
  refPosEci = sceneSeq.refPosEci;
  refVelEci = sceneSeq.refVelEci;
else
  if ~isfield(sceneSeq, 'sceneCell') || numel(sceneSeq.sceneCell) ~= numFrame
    error('crbPilotMfDoaDoppler:MissingSceneCell', ...
      'Aggregated dynamic states are incomplete and sceneSeq.sceneCell is unavailable.');
  end

  rotMatCell = cell(numFrame, numSat);
  satPosEci = zeros(3, numSat, numFrame);
  satVelEci = zeros(3, numSat, numFrame);
  refPosEci = zeros(3, numFrame);
  refVelEci = zeros(3, numFrame);

  for iFrame = 1:numFrame
    sceneTmp = sceneSeq.sceneCell{iFrame};
    if ~isfield(sceneTmp, 'rotMat') || ~iscell(sceneTmp.rotMat) || numel(sceneTmp.rotMat) ~= numSat
      error('crbPilotMfDoaDoppler:InvalidRotMat', ...
        'Each frame scene must contain a 1xNs cell array scene.rotMat.');
    end
    if ~isfield(sceneTmp, 'satPosEci') || ~isequal(size(sceneTmp.satPosEci), [3, numSat])
      error('crbPilotMfDoaDoppler:InvalidSatPos', ...
        'Each frame scene must contain scene.satPosEci with size 3xNs.');
    end
    if ~isfield(sceneTmp, 'satVelEci') || ~isequal(size(sceneTmp.satVelEci), [3, numSat])
      error('crbPilotMfDoaDoppler:InvalidSatVel', ...
        'Each frame scene must contain scene.satVelEci with size 3xNs.');
    end
    if ~isfield(sceneTmp, 'ref') || ~isstruct(sceneTmp.ref) || ...
        ~isfield(sceneTmp.ref, 'posEci') || ~isfield(sceneTmp.ref, 'velEci')
      error('crbPilotMfDoaDoppler:InvalidRefState', ...
        'Each frame scene must contain scene.ref.posEci and scene.ref.velEci.');
    end

    rotMatCell(iFrame, :) = reshape(sceneTmp.rotMat, 1, []);
    satPosEci(:, :, iFrame) = sceneTmp.satPosEci;
    satVelEci(:, :, iFrame) = sceneTmp.satVelEci;
    refPosEci(:, iFrame) = sceneTmp.ref.posEci;
    refVelEci(:, iFrame) = sceneTmp.ref.velEci;
  end
end

if ~iscell(rotMatCell) || ~isequal(size(rotMatCell), [numFrame, numSat])
  error('crbPilotMfDoaDoppler:InvalidRotMatSize', ...
    'sceneSeq.rotMat must have size Nf x Ns.');
end
if ~isequal(size(satPosEci), [3, numSat, numFrame])
  error('crbPilotMfDoaDoppler:InvalidSatPosSize', ...
    'sceneSeq.satPosEci must have size 3xNs x Nf.');
end
if ~isequal(size(satVelEci), [3, numSat, numFrame])
  error('crbPilotMfDoaDoppler:InvalidSatVelSize', ...
    'sceneSeq.satVelEci must have size 3xNs x Nf.');
end
if ~isequal(size(refPosEci), [3, numFrame])
  error('crbPilotMfDoaDoppler:InvalidRefPosSize', ...
    'sceneSeq.refPosEci must have size 3xNf.');
end
if ~isequal(size(refVelEci), [3, numFrame])
  error('crbPilotMfDoaDoppler:InvalidRefVelSize', ...
    'sceneSeq.refVelEci must have size 3xNf.');
end

utcVec = localParseUtcVec(sceneSeq, numFrame);
end


