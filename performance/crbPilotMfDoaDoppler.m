function [pilotCell, numSampleFrame] = localParsePilotWave(pilotWave, numFrame)
%LOCALPARSEPILOTWAVE Parse pilot waveform template(s).

pilotInput = localNormalizePilotInput(pilotWave, numFrame);
pilotCell = cell(1, numFrame);
numSampleFrame = zeros(1, numFrame);

for iFrame = 1:numFrame
  currentPilot = pilotInput{iFrame};

  if ~isnumeric(currentPilot) || isempty(currentPilot)
    error('crbPilotMfDoaDoppler:InvalidPilotWave', ...
      'Each pilotWave entry must be a non-empty numeric vector.');
  end

  if isvector(currentPilot)
    pilotRow = reshape(currentPilot, 1, []);
  elseif size(currentPilot, 1) == 1
    pilotRow = currentPilot;
  elseif size(currentPilot, 2) == 1
    pilotRow = currentPilot.';
  else
    error('crbPilotMfDoaDoppler:InvalidPilotWaveSize', ...
      'This CRB function only supports single-source pilotWave inputs.');
  end

  if any(~isfinite(real(pilotRow(:)))) || any(~isfinite(imag(pilotRow(:))))
    error('crbPilotMfDoaDoppler:InvalidPilotWaveValue', ...
      'pilotWave must contain finite values.');
  end

  pilotCell{iFrame} = pilotRow;
  numSampleFrame(iFrame) = numel(pilotRow);
end

end


