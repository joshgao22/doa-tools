end

end


function frameMask = localParseFrameMask(sceneSeq, modelOpt, numSat, numFrame)
%LOCALPARSEFRAMEMASK Parse the validity mask over satellite-frame blocks.

if ~isempty(modelOpt.frameMask)
  frameMask = logical(modelOpt.frameMask);
  if isequal(size(frameMask), [numSat, numFrame])
    return;
  elseif isequal(size(frameMask), [numFrame, numSat])
    frameMask = frameMask.';
    return;
  else
    error('crbPilotMfDoaDoppler:InvalidFrameMask', ...
      'modelOpt.frameMask must have size Ns x Nf or Nf x Ns.');
  end
end

if logical(modelOpt.useAccessMask) && isfield(sceneSeq, 'access') && ~isempty(sceneSeq.access)
  access = sceneSeq.access;

  if ndims(access) == 3
    if size(access, 1) ~= numSat || size(access, 3) ~= numFrame
      error('crbPilotMfDoaDoppler:InvalidAccessMask', ...
        'sceneSeq.access must have size Ns x Nu x Nf.');
    end
    frameMask = reshape(squeeze(all(access, 2)), numSat, numFrame);
  elseif ismatrix(access)
    if isequal(size(access), [numSat, numFrame])
      frameMask = access;
    elseif isequal(size(access), [numFrame, numSat])
      frameMask = access.';
    else
      error('crbPilotMfDoaDoppler:InvalidAccessMask', ...
        'sceneSeq.access has an unsupported size.');
    end
  else
    error('crbPilotMfDoaDoppler:InvalidAccessMask', ...
      'sceneSeq.access has an unsupported number of dimensions.');
  end

  frameMask = logical(frameMask);
else
  frameMask = true(numSat, numFrame);
end

end


function valMat = localExpandPerBlock(valIn, numSat, numFrame, nameStr)
%LOCALEXPANDPERBLOCK Expand scalar / per-satellite / per-block inputs.

if isscalar(valIn)
  valMat = repmat(valIn, numSat, numFrame);
  return;
end

if isvector(valIn)
  valVec = reshape(valIn, [], 1);
  if numel(valVec) == numSat
    valMat = repmat(valVec, 1, numFrame);
    return;
  elseif numSat == 1 && numel(valVec) == numFrame
    valMat = reshape(valVec, 1, []);
    return;
  elseif numFrame == 1 && numel(valVec) == numSat
    valMat = reshape(valVec, [], 1);
    return;
  end
end

if ismatrix(valIn) && isequal(size(valIn), [numSat, numFrame])
  valMat = valIn;
  return;
elseif ismatrix(valIn) && isequal(size(valIn), [numFrame, numSat])
  valMat = valIn.';
  return;
elseif ismatrix(valIn) && isequal(size(valIn), [numSat, 1])
  valMat = repmat(valIn, 1, numFrame);
  return;
elseif ismatrix(valIn) && isequal(size(valIn), [1, numSat])
  valMat = repmat(valIn(:), 1, numFrame);
  return;
end

error('crbPilotMfDoaDoppler:InvalidBlockInputSize', ...
  '%s must be scalar, Ns x 1, or Ns x Nf.', nameStr);
end


