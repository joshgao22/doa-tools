  otherwise
    error('crbPilotMfDoaDoppler:InvalidFdRateMode', ...
      'Unsupported fdRate mode: %s.', fdRateMode);
end

modelType = [phaseTag, rateTag];
end


function [intercept, slope, fitMat] = localFitSatLine(timeOffsetSec, valueSeq)
%LOCALFITSATLINE Fit one line per satellite over the frame offsets.

fitMat = localBuildLineFitMatrix(timeOffsetSec);
[intercept, slope] = localApplyLineFit(fitMat, valueSeq);
end


function fitMat = localBuildLineFitMatrix(timeOffsetSec)
%LOCALBUILDLINEFITMATRIX Build the LS matrix for line fitting.

timeOffsetSec = reshape(timeOffsetSec, [], 1);
numFrame = numel(timeOffsetSec);
if numFrame == 1 || max(abs(timeOffsetSec - timeOffsetSec(1))) <= eps(max(abs(timeOffsetSec)))
  fitMat = [ones(1, numFrame) / numFrame; zeros(1, numFrame)];
  return;
end

designMat = [ones(numFrame, 1), timeOffsetSec];
fitMat = pinv(designMat);
end


function [intercept, slope] = localApplyLineFit(fitMat, valueSeq)
%LOCALAPPLYLINEFIT Apply the line-fit matrix to one value sequence per row.

coef = fitMat * valueSeq.';
intercept = coef(1, :).';
slope = coef(2, :).';
end


