function [crb, aux] = crbPilotMfDoaDoppler(sceneSeq, pilotWave, carrierFreq, sampleRate, ...
  doaParam, fdRef, fdRate, pathGain, noiseVar, modelOpt)
%CRBPILOTMFDOADOPPLER Deterministic-model CRB for multi-frame pilot DoA-Doppler estimation.
% Unified CRB entry for the continuous-phase, relaxed, and independent
% multi-frame models. The formal mainline matches the estimator route:
% framewise steering, reference-time global DoA, reference-satellite
% Doppler as the main parameter, and fdRate controlled by fdRateMode.
%
%Syntax:
%  crb = crbPilotMfDoaDoppler(sceneSeq, pilotWave, carrierFreq, sampleRate, ...
%    doaParam, fdRef, fdRate, pathGain, noiseVar)
%
%  [crb, aux] = crbPilotMfDoaDoppler(sceneSeq, pilotWave, carrierFreq, sampleRate, ...
%    doaParam, fdRef, fdRate, pathGain, noiseVar, modelOpt)
%
%Inputs:
%  sceneSeq     - Multi-frame scene structure generated by genMultiFrameScene.
%                 Required fields:
%                   .numFrame, .numSat, .timeOffsetSec
%                 Preferred aggregated fields:
%                   .array, .rotMat, .satPosEci, .satVelEci,
%                   .refPosEci, .refVelEci, .utcVec
%                 Fallback source:
%                   .sceneCell
%
%  pilotWave    - Known pilot waveform template(s).
%                 - vector / 1xT / Tx1 : shared pilot for all frames
%                 - 1xNf cell          : one pilot vector per frame
%
%  carrierFreq  - Carrier frequency in Hz.
%
%  sampleRate   - Sampling rate in Hz.
%                 Used when modelOpt.timeInput is not provided.
%
%  doaParam     - 2x1 global DoA parameter vector.
%                 angle mode : [eciAz; eciEl] in radians
%                 latlon mode: [lat; lon] in degrees
%
%  fdRef        - Scalar reference Doppler intercept in Hz.
%
%  fdRate       - Scalar reference Doppler rate in Hz/s.
%                 Interpreted according to modelOpt.fdRateMode:
%                   'unknown' : nuisance parameter to be eliminated
%                   'known'   : known side information
%                   'zero'    : fixed to zero, input fdRate is ignored
%
%  pathGain     - Deterministic per-block complex path gains.
%                 Supported layouts:
%                   - scalar : same gain for all satellites and frames
%                   - Ns x 1 : one gain per satellite, shared by frames
%                   - Ns x Nf: one gain per satellite-frame block
%
%  noiseVar     - Noise power per block.
%                 Supported layouts:
%                   - scalar : same noise power for all satellites and frames
%                   - Ns x 1 : one noise power per satellite, shared by frames
%                   - Ns x Nf: one noise power per satellite-frame block
%
%  modelOpt     - Optional settings structure.
%                 .doaType            : 'angle' or 'latlon', default 'angle'
%                 .lightSpeed         : propagation speed in m/s,
%                                       default 299792458
%                 .timeInput          : scalar sample rate, common time axis,
%                                       or 1xNf cell of per-frame time inputs,
%                                       default sampleRate
%                 .steeringMode       : 'frozenRef' or 'framewise',
%                                       default 'frozenRef'
%                 .steeringRefFrameIdx: reference frame index used by
%                                       frozen steering, default sceneSeq.refFrameIdx
%                 .useAccessMask      : whether to use sceneSeq.access as
%                                       the frame validity mask, default true
%                 .frameMask          : optional Ns x Nf logical mask
%                                       overriding sceneSeq.access
%                 .jacOpt             : options passed to
%                                       buildDynDoaDopplerJacobian,
%                                       default struct()
%                 .phaseMode          : 'continuous', 'relaxed', or
%                                       'independent', default 'continuous'
%                 .fdRateMode         : 'unknown', 'known', or 'zero',
%                                       default 'unknown'
%                 .pathPhaseTol       : consistency tolerance used when
%                                       projecting pathGain onto the
%                                       continuous-phase manifold,
%                                       default 1e-8
%
%Outputs:
%  crb          - CRB matrix of the interest parameters
%                 [doaParam(1); doaParam(2); fdRef].
%
%  aux          - Auxiliary structure with fields:
%                 .modelType
%                 .doaType
%                 .phaseMode
%                 .fdRateMode
%                 .numSat
%                 .numFrame
%                 .timeOffsetSec
%                 .paramNameModel
%                 .paramNameFull
%                 .paramNameInterest
%                 .paramName          (alias of .paramNameInterest)
%                 .activeParamIdx
%                 .nuisanceParamIdx
%                 .doaState
%                 .jacobianModel
%                 .jacobianFull
%                 .jacobianInterest
%                 .jacobian          (alias of .jacobianFull)
%                 .fimFull
%                 .fimInterest
%                 .fim               (alias of .fimInterest)
%                 .crbFull
%                 .pathGain
%                 .pathGainInput
%                 .noiseVar
%                 .frameMask
%                 .pilotCell
%                 .timeInputCell
%                 .wavelength
%
%Description:
%  Builds the deterministic-model CRB for the single-source multi-frame
%  pilot DoA-Doppler model. One unified entry supports the following phase
%  structures:
%
%    - structured continuous phase,
%    - relaxed continuous phase, and
%    - independent per-frame phase.
%
%  The returned CRB is always for the interest parameters
%  [doaParam(1); doaParam(2); fdRef]. Phase, amplitude, and optionally
%  fdRate are treated as nuisance parameters and are eliminated through the
%  Schur complement.
%
%Notes:
%  - This function is single-source only.
%  - The first two CRB dimensions inherit the units of doaParam.
%  - The third dimension is always in Hz.
%
%See also:
%  crbPilotSfDoaDoppler, buildDynDoaDopplerJacobian,
%  estimatorDoaDopplerMlePilotMfOpt

arguments
  sceneSeq (1,1) struct
  pilotWave
  carrierFreq (1,1) {mustBePositive, mustBeNumeric, mustBeFinite}
  sampleRate (1,1) {mustBePositive, mustBeNumeric, mustBeFinite}
  doaParam {mustBeNumeric, mustBeFinite}
  fdRef (1,1) {mustBeNumeric, mustBeFinite}
  fdRate (1,1) {mustBeNumeric, mustBeFinite}
  pathGain {mustBeNumeric, mustBeFinite}
  noiseVar {mustBeNumeric, mustBeFinite}
  modelOpt (1,1) struct = struct()
end

modelOpt = localParseModelOpt(modelOpt, sceneSeq, sampleRate);
model = localBuildModel(sceneSeq, pilotWave, carrierFreq, modelOpt);
numSat = model.numSat;
numFrame = model.numFrame;

doaParam = reshape(doaParam, [], 1);
if numel(doaParam) ~= 2
  error('crbPilotMfDoaDoppler:InvalidDoaParamSize', ...
    'doaParam must contain exactly two entries.');
end

pathGain = localExpandPerBlock(pathGain, numSat, numFrame, 'pathGain');
noiseVar = localExpandPerBlock(noiseVar, numSat, numFrame, 'noiseVar');
if any(noiseVar(:) <= 0)
  error('crbPilotMfDoaDoppler:InvalidNoiseVar', ...
    'noiseVar must be positive.');
end

[pathInfo, pathWarn] = localBuildPathInfo(pathGain, model.frameMask, modelOpt);
[dynState, jacobianInfo] = localBuildDynamicState(model, doaParam, fdRef, fdRate, modelOpt);
[fimFull, blockAux] = localBuildFullFim(model, dynState, jacobianInfo, pathInfo, noiseVar, modelOpt);
paramNameFull = [jacobianInfo.paramNameActive, pathInfo.phaseName, pathInfo.ampName];

fullParamIdx = 1:numel(paramNameFull);
interestIdx = 1:3;
nuisanceIdx = setdiff(fullParamIdx, interestIdx);
[fimInterest, crb, crbFull] = localExtractInterestCrb(fimFull, interestIdx, nuisanceIdx);

if nargout >= 2
  aux = struct();
  aux.modelType = localGetModelType(modelOpt.phaseMode, modelOpt.fdRateMode);
  aux.doaType = modelOpt.doaType;
  aux.phaseMode = modelOpt.phaseMode;
  aux.fdRateMode = modelOpt.fdRateMode;
  aux.numSat = model.numSat;
  aux.numFrame = model.numFrame;
  aux.timeOffsetSec = model.timeOffsetSec;
  aux.paramNameModel = jacobianInfo.paramNameModel;
  aux.paramNameFull = paramNameFull;
  aux.paramNameInterest = jacobianInfo.paramNameInterest;
  aux.paramName = aux.paramNameInterest;
  aux.activeParamIdx = 1:numel(jacobianInfo.paramNameActive);
  aux.nuisanceParamIdx = nuisanceIdx;
  aux.doaState = dynState.doaState;
  aux.jacobianModel = jacobianInfo.model;
  aux.jacobianFull = jacobianInfo.active;
  aux.jacobianInterest = jacobianInfo.interest;
  aux.jacobian = aux.jacobianFull;
  aux.fimFull = fimFull;
  aux.fimInterest = fimInterest;
  aux.fim = fimInterest;
  aux.crbFull = crbFull;
  aux.atomCell = blockAux.atomCell;
  aux.dAtomCell = blockAux.dAtomCell;
  aux.pathGain = pathInfo.gainModel;
  aux.pathGainInput = pathGain;
  aux.pathAmp = pathInfo.amp;
  aux.pathPhase = pathInfo.phase;
  aux.pathConsistency = pathWarn;
  aux.noiseVar = noiseVar;
  aux.frameMask = model.frameMask;
  aux.pilotCell = model.pilotCell;
  aux.timeInputCell = model.timeInputCell;
  aux.wavelength = model.wavelength;
  aux.blockIndex = blockAux.blockIndex;
  aux.fdSat = dynState.fdSat;
  aux.fdRateSat = dynState.fdRateSat;
  aux.fdSatJac = dynState.dFdSat;
  aux.fdRateSatJac = dynState.dFdRate;
else
  aux = [];
end

end


function modelOpt = localParseModelOpt(modelOpt, sceneSeq, sampleRate)
%LOCALPARSEMODELOPT Parse optional CRB settings.

if ~isfield(modelOpt, 'doaType') || isempty(modelOpt.doaType)
  modelOpt.doaType = 'angle';
end
if ~isfield(modelOpt, 'lightSpeed') || isempty(modelOpt.lightSpeed)
  modelOpt.lightSpeed = 299792458;
end
if ~isfield(modelOpt, 'timeInput') || isempty(modelOpt.timeInput)
  modelOpt.timeInput = sampleRate;
end
if ~isfield(modelOpt, 'steeringMode') || isempty(modelOpt.steeringMode)
  modelOpt.steeringMode = 'framewise';
end
if ~isfield(modelOpt, 'steeringRefFrameIdx') || isempty(modelOpt.steeringRefFrameIdx)
  if isfield(sceneSeq, 'refFrameIdx') && ~isempty(sceneSeq.refFrameIdx)
    modelOpt.steeringRefFrameIdx = sceneSeq.refFrameIdx;
  else
    modelOpt.steeringRefFrameIdx = round((sceneSeq.numFrame + 1) / 2);
  end
end
if ~isfield(modelOpt, 'useAccessMask') || isempty(modelOpt.useAccessMask)
  modelOpt.useAccessMask = true;
end
if ~isfield(modelOpt, 'frameMask')
  modelOpt.frameMask = [];
end
if ~isfield(modelOpt, 'jacOpt') || isempty(modelOpt.jacOpt)
  modelOpt.jacOpt = struct();
end
if ~isfield(modelOpt, 'phaseMode') || isempty(modelOpt.phaseMode)
  modelOpt.phaseMode = 'continuous';
end
if ~isfield(modelOpt, 'fdRateMode') || isempty(modelOpt.fdRateMode)
  modelOpt.fdRateMode = 'unknown';
end
if ~isfield(modelOpt, 'pathPhaseTol') || isempty(modelOpt.pathPhaseTol)
  modelOpt.pathPhaseTol = 1e-8;
end

modelOpt.doaType = validatestring(modelOpt.doaType, {'angle', 'latlon'}, ...
  mfilename, 'modelOpt.doaType');
modelOpt.phaseMode = validatestring(modelOpt.phaseMode, ...
  {'continuous', 'relaxed', 'independent'}, mfilename, 'modelOpt.phaseMode');
modelOpt.fdRateMode = validatestring(modelOpt.fdRateMode, ...
  {'unknown', 'known', 'zero'}, mfilename, 'modelOpt.fdRateMode');
if ~isscalar(modelOpt.lightSpeed) || ~isfinite(modelOpt.lightSpeed) || modelOpt.lightSpeed <= 0
  error('crbPilotMfDoaDoppler:InvalidLightSpeed', ...
    'modelOpt.lightSpeed must be a positive finite scalar.');
end
if ~isstruct(modelOpt.jacOpt)
  error('crbPilotMfDoaDoppler:InvalidJacOpt', ...
    'modelOpt.jacOpt must be a struct.');
end
if ~isscalar(modelOpt.pathPhaseTol) || ~isfinite(modelOpt.pathPhaseTol) || modelOpt.pathPhaseTol < 0
  error('crbPilotMfDoaDoppler:InvalidPathPhaseTol', ...
    'modelOpt.pathPhaseTol must be a nonnegative finite scalar.');
end

if isstring(modelOpt.steeringMode)
  modelOpt.steeringMode = char(modelOpt.steeringMode);
end
modelOpt.steeringMode = lower(strtrim(modelOpt.steeringMode));
if ~ismember(modelOpt.steeringMode, {'frozenref', 'framewise'})
  error('crbPilotMfDoaDoppler:InvalidSteeringMode', ...
    'modelOpt.steeringMode must be ''frozenRef'' or ''framewise''.');
end

if ~isscalar(modelOpt.steeringRefFrameIdx) || ~isnumeric(modelOpt.steeringRefFrameIdx) || ...
    ~isfinite(modelOpt.steeringRefFrameIdx) || mod(modelOpt.steeringRefFrameIdx, 1) ~= 0 || ...
    modelOpt.steeringRefFrameIdx < 1 || modelOpt.steeringRefFrameIdx > sceneSeq.numFrame
  error('crbPilotMfDoaDoppler:InvalidSteeringRefFrameIdx', ...
    'modelOpt.steeringRefFrameIdx must be an integer within [1, numFrame].');
end

end


function model = localBuildModel(sceneSeq, pilotWave, carrierFreq, modelOpt)
%LOCALBUILDMODEL Build fixed dynamic quantities used by the CRB.

localCheckSceneSeq(sceneSeq);

numFrame = sceneSeq.numFrame;
numSat = sceneSeq.numSat;
arrayCell = localParseSceneArray(sceneSeq, numSat);
[numElement, rotMatCell, satPosEci, satVelEci, refPosEci, refVelEci, utcVec] = ...
  localParseSceneKinematics(sceneSeq, numSat);
[pilotCell, numSampleFrame] = localParsePilotWave(pilotWave, numFrame);
timeInputCell = localParseTimeInput(modelOpt.timeInput, numFrame, numSampleFrame);
frameMask = localParseFrameMask(sceneSeq, modelOpt, numSat, numFrame);

model = struct();
model.array = arrayCell;
model.numElement = numElement;
model.numSat = numSat;
model.numFrame = numFrame;
model.wavelength = modelOpt.lightSpeed / carrierFreq;
model.pilotCell = pilotCell;
model.timeInputCell = timeInputCell;
model.frameMask = frameMask;
model.timeOffsetSec = reshape(sceneSeq.timeOffsetSec, 1, []);

geom = struct();
geom.doaType = modelOpt.doaType;
geom.rotMat = rotMatCell;
geom.wavelength = model.wavelength;
geom.timeOffsetSec = model.timeOffsetSec;
geom.steeringMode = modelOpt.steeringMode;
geom.steeringRefFrameIdx = modelOpt.steeringRefFrameIdx;
geom.satPosEci = satPosEci;
geom.satVelEci = satVelEci;
geom.refPosEci = refPosEci;
geom.refVelEci = refVelEci;
geom.relSatVelEci = satVelEci - reshape(refVelEci, 3, 1, numFrame);
geom.utcVec = utcVec;
geom.scene = sceneSeq;

model.geom = geom;
end


function [pathInfo, warnInfo] = localBuildPathInfo(pathGain, frameMask, modelOpt)
%LOCALBUILDPATHINFO Build nuisance-path parameterization information.

[numSat, numFrame] = size(pathGain);
phaseMode = modelOpt.phaseMode;
validBlock = logical(frameMask);
ampTol = max(modelOpt.pathPhaseTol, eps);

pathInfo = struct();
pathInfo.phaseMode = phaseMode;
pathInfo.amp = nan(numSat, numFrame);
pathInfo.phase = nan(numSat, numFrame);
pathInfo.gainModel = nan(numSat, numFrame);
pathInfo.phaseParamIdx = zeros(numSat, numFrame);
pathInfo.ampParamIdx = zeros(numSat, numFrame);

warnInfo = struct();
warnInfo.isProjected = false;
warnInfo.maxPhaseResidual = 0;
warnInfo.maxImagResidual = 0;
warnInfo.message = '';

switch phaseMode
  case 'continuous'
    phaseCount = 0;
    ampCount = 0;
    phaseName = {};
    ampName = {};

    for iSat = 1:numSat
      validIdx = find(validBlock(iSat, :));
      if isempty(validIdx)
        continue;
      end

      gainVec = pathGain(iSat, validIdx);
      magVec = abs(gainVec);
      nzMask = magVec > ampTol;
      if any(nzMask)
        phaseSat = angle(sum(gainVec(nzMask)));
        rotGain = gainVec .* exp(-1j * phaseSat);
        if mean(real(rotGain(nzMask))) < 0
          phaseSat = phaseSat + pi;
          rotGain = gainVec .* exp(-1j * phaseSat);
        end
      else
        phaseSat = 0;
        rotGain = gainVec;
      end

      ampVec = real(rotGain);
      imagResidual = imag(rotGain);
      maxMag = max(1, max(abs(gainVec)));
      warnInfo.maxImagResidual = max(warnInfo.maxImagResidual, max(abs(imagResidual)) / maxMag);
      if any(abs(imagResidual) > modelOpt.pathPhaseTol * maxMag)
        warnInfo.isProjected = true;
      end

      ampVec = max(ampVec, 0);
      gainProj = ampVec .* exp(1j * phaseSat);

      pathInfo.amp(iSat, validIdx) = ampVec;
      pathInfo.phase(iSat, validIdx) = phaseSat;
      pathInfo.gainModel(iSat, validIdx) = gainProj;
      ampStart = ampCount + 1;
      pathInfo.ampParamIdx(iSat, validIdx) = ampStart:(ampStart + numel(validIdx) - 1);
      ampCount = ampCount + numel(validIdx);

      if any(ampVec > ampTol)
        phaseCount = phaseCount + 1;
        pathInfo.phaseParamIdx(iSat, validIdx) = phaseCount;
        phaseName{phaseCount} = sprintf('phiSat%d', iSat); %#ok<AGROW>
      end

      for iLocal = 1:numel(validIdx)
        ampName{ampStart + iLocal - 1} = ...
          sprintf('ampSat%dFrame%d', iSat, validIdx(iLocal)); %#ok<AGROW>
      end
    end

    pathInfo.phaseCount = phaseCount;
    pathInfo.ampCount = ampCount;
    pathInfo.phaseName = phaseName;
    pathInfo.ampName = ampName;

  case {'independent', 'relaxed'}
    phaseCount = 0;
    ampCount = 0;
    phaseName = {};
    ampName = {};

    for iSat = 1:numSat
      for iFrame = 1:numFrame
        if ~validBlock(iSat, iFrame)
          continue;
        end

        gainVal = pathGain(iSat, iFrame);
        ampVal = abs(gainVal);
        phaseVal = angle(gainVal);

        pathInfo.amp(iSat, iFrame) = ampVal;
        pathInfo.phase(iSat, iFrame) = phaseVal;
        pathInfo.gainModel(iSat, iFrame) = ampVal * exp(1j * phaseVal);

        ampCount = ampCount + 1;
        pathInfo.ampParamIdx(iSat, iFrame) = ampCount;
        ampName{ampCount} = sprintf('ampSat%dFrame%d', iSat, iFrame); %#ok<AGROW>

        if ampVal > ampTol
          phaseCount = phaseCount + 1;
          pathInfo.phaseParamIdx(iSat, iFrame) = phaseCount;
          phaseName{phaseCount} = sprintf('phaseSat%dFrame%d', iSat, iFrame); %#ok<AGROW>
        end
      end
    end

    pathInfo.phaseCount = phaseCount;
    pathInfo.ampCount = ampCount;
    pathInfo.phaseName = phaseName;
    pathInfo.ampName = ampName;

  otherwise
    error('crbPilotMfDoaDoppler:InvalidPhaseMode', ...
      'Unsupported phase mode: %s.', phaseMode);
end

if warnInfo.isProjected
  warnInfo.message = ['pathGain is not fully consistent with the structured ', ...
    'continuous-phase model. The CRB uses the projected common-phase path gains.'];
  warning('crbPilotMfDoaDoppler:ProjectedContinuousPathGain', '%s', warnInfo.message);
end

end


function [dynState, jacobianInfo] = localBuildDynamicState(model, doaParam, fdRef, fdRate, modelOpt)
%LOCALBUILDDYNAMICSTATE Build dynamic line-fit states and Jacobians.

switch modelOpt.fdRateMode
  case 'zero'
    fdRateUsed = 0;
  otherwise
    fdRateUsed = fdRate;
end

[jacobianModel, doaState] = buildDynDoaDopplerJacobian(model.geom, doaParam, fdRef, fdRateUsed, modelOpt.jacOpt);
if numel(jacobianModel.paramName) ~= 4
  error('crbPilotMfDoaDoppler:UnexpectedParamCount', ...
    'The dynamic Jacobian must return four parameters [doaParam; fdRef; fdRate].');
end

[fdSat, fdRateSat, fitMat] = localFitSatLine(model.timeOffsetSec, doaState.fd);
numParamModel = numel(jacobianModel.paramName);
dFdSat = zeros(model.numSat, numParamModel);
dFdRate = zeros(model.numSat, numParamModel);
for iParam = 1:numParamModel
  derivSeq = squeeze(jacobianModel.fd(:, :, iParam));
  if isvector(derivSeq)
    derivSeq = reshape(derivSeq, model.numSat, model.numFrame);
  end
  [dFdSat(:, iParam), dFdRate(:, iParam)] = localApplyLineFit(fitMat, derivSeq);
end

switch modelOpt.fdRateMode
  case 'unknown'
    activeModelIdx = 1:4;
  case {'known', 'zero'}
    activeModelIdx = 1:3;
  otherwise
    error('crbPilotMfDoaDoppler:InvalidFdRateMode', ...
      'Unsupported fdRate mode: %s.', modelOpt.fdRateMode);
end

jacobianActive = struct();
jacobianActive.paramName = jacobianModel.paramName(activeModelIdx);
jacobianActive.localDoa = jacobianModel.localDoa;
jacobianActive.fdSat = dFdSat(:, activeModelIdx);
jacobianActive.fdRate = dFdRate(:, activeModelIdx);
jacobianActive.fdSeq = jacobianModel.fd(:, :, activeModelIdx);

jacobianInterest = struct();
jacobianInterest.paramName = jacobianModel.paramName(1:3);
jacobianInterest.localDoa = jacobianModel.localDoa;
jacobianInterest.fdSat = dFdSat(:, 1:3);
jacobianInterest.fdRate = dFdRate(:, 1:3);
jacobianInterest.fdSeq = jacobianModel.fd(:, :, 1:3);

dynState = struct();
dynState.doaState = doaState;
dynState.fdSat = fdSat;
dynState.fdRateSat = fdRateSat;
dynState.dFdSat = dFdSat;
dynState.dFdRate = dFdRate;
dynState.fitMat = fitMat;

jacobianInfo = struct();
jacobianInfo.model = jacobianModel;
jacobianInfo.active = jacobianActive;
jacobianInfo.interest = jacobianInterest;
jacobianInfo.activeModelIdx = activeModelIdx;
jacobianInfo.paramNameModel = jacobianModel.paramName;
jacobianInfo.paramNameActive = jacobianActive.paramName;
jacobianInfo.paramNameInterest = jacobianInterest.paramName;
end


function [fimFull, blockAux] = localBuildFullFim(model, dynState, jacobianInfo, pathInfo, noiseVar, modelOpt)
%LOCALBUILDFULLFIM Assemble the complete deterministic FIM.

numStruct = numel(jacobianInfo.paramNameActive);
numPhase = pathInfo.phaseCount;
numAmp = pathInfo.ampCount;
numFull = numStruct + numPhase + numAmp;

phaseOffset = numStruct;
ampOffset = numStruct + numPhase;

phaseParamIdx = zeros(size(pathInfo.phaseParamIdx));
ampParamIdx = zeros(size(pathInfo.ampParamIdx));
phaseMask = pathInfo.phaseParamIdx > 0;
ampMask = pathInfo.ampParamIdx > 0;
phaseParamIdx(phaseMask) = phaseOffset + pathInfo.phaseParamIdx(phaseMask);
ampParamIdx(ampMask) = ampOffset + pathInfo.ampParamIdx(ampMask);

fimFull = zeros(numFull, numFull);
blockAux = struct();
blockAux.atomCell = cell(model.numFrame, model.numSat);
blockAux.dAtomCell = cell(model.numFrame, model.numSat);
blockAux.blockIndex = zeros(model.numSat, model.numFrame);

blockCounter = 0;
for iFrame = 1:model.numFrame
  pilotVec = model.pilotCell{iFrame};
  timeLoc = localTimeAxis(model.timeInputCell{iFrame}, numel(pilotVec));
  tauFrame = model.timeOffsetSec(iFrame);

  for iSat = 1:model.numSat
    if ~model.frameMask(iSat, iFrame)
      continue;
    end

    blockCounter = blockCounter + 1;
    blockAux.blockIndex(iSat, iFrame) = blockCounter;

    localDoa = dynState.doaState.localDoaCell{iFrame, iSat};
    [steeringVec, dSteering] = steeringMatrix(model.array{iSat}, model.wavelength, localDoa);
    steeringVec = steeringVec(:);

    dLocalDoa = zeros(2, numStruct);
    for iParam = 1:min(2, numStruct)
      iModel = jacobianInfo.activeModelIdx(iParam);
      dLocalDoa(:, iParam) = jacobianInfo.model.localDoa(:, iModel, iSat, iFrame);
    end

    dFdSat = dynState.dFdSat(iSat, jacobianInfo.activeModelIdx);
    dFdRate = dynState.dFdRate(iSat, jacobianInfo.activeModelIdx);
    fdSat = dynState.fdSat(iSat);
    fdRateSat = dynState.fdRateSat(iSat);

    [pilotFd, dPilotStruct] = localBuildPilotStructJacobian( ...
      pilotVec, timeLoc, tauFrame, fdSat, fdRateSat, dFdSat, dFdRate, modelOpt.phaseMode);

    atomMat = steeringVec * pilotFd;
    atomVec = atomMat(:);
    atomEnergy = real(atomVec' * atomVec);
    if ~(isfinite(atomEnergy) && atomEnergy > 0)
      error('crbPilotMfDoaDoppler:InvalidAtomEnergy', ...
        'The deterministic atom energy must be positive at satellite %d, frame %d.', ...
        iSat, iFrame);
    end

    dAtomMat = zeros(numel(atomVec), numStruct);
    for iParam = 1:numStruct
      dSteeringCurrent = dSteering.az * dLocalDoa(1, iParam) + ...
        dSteering.el * dLocalDoa(2, iParam);
      dAtomCurrent = dSteeringCurrent * pilotFd + steeringVec * dPilotStruct(:, iParam).';
      dAtomMat(:, iParam) = dAtomCurrent(:);
    end

    gainVal = pathInfo.gainModel(iSat, iFrame);
    ampVal = pathInfo.amp(iSat, iFrame);
    phaseVal = pathInfo.phase(iSat, iFrame);
    if ~isfinite(phaseVal)
      phaseVal = 0;
    end

    colIdx = 1:numStruct;
    derivCell = {gainVal * dAtomMat};

    phaseIdx = phaseParamIdx(iSat, iFrame);
    if phaseIdx > 0 && ampVal > max(modelOpt.pathPhaseTol, eps)
      colIdx = [colIdx, phaseIdx]; %#ok<AGROW>
      derivCell{end + 1} = 1j * gainVal * atomVec; %#ok<AGROW>
    end

    ampIdx = ampParamIdx(iSat, iFrame);
    if ampIdx > 0
      colIdx = [colIdx, ampIdx]; %#ok<AGROW>
      derivCell{end + 1} = exp(1j * phaseVal) * atomVec; %#ok<AGROW>
    end

    derivBlock = localConcatDeriv(derivCell);
    fimBlock = 2 / noiseVar(iSat, iFrame) * real(derivBlock' * derivBlock);
    fimBlock = 0.5 * (fimBlock + fimBlock.');
    fimFull(colIdx, colIdx) = fimFull(colIdx, colIdx) + fimBlock;

    blockAux.atomCell{iFrame, iSat} = atomMat;
    blockAux.dAtomCell{iFrame, iSat} = dAtomMat;
  end
end

fimFull = 0.5 * (fimFull + fimFull.');
if rcond(fimFull + eye(size(fimFull)) * eps) < 1e-12
  warning('crbPilotMfDoaDoppler:IllConditionedFim', ...
    'The full FIM is singular or ill-conditioned. CRB may be unreliable.');
end

end


function derivBlock = localConcatDeriv(derivCell)
%LOCALCONCATDERIV Concatenate derivative blocks column-wise.

numCol = 0;
numRow = [];
for iCell = 1:numel(derivCell)
  currentDeriv = derivCell{iCell};
  if isempty(currentDeriv)
    continue;
  end
  if isempty(numRow)
    numRow = size(currentDeriv, 1);
  end
  numCol = numCol + size(currentDeriv, 2);
end

derivBlock = zeros(numRow, numCol);
colStart = 1;
for iCell = 1:numel(derivCell)
  currentDeriv = derivCell{iCell};
  if isempty(currentDeriv)
    continue;
  end
  colEnd = colStart + size(currentDeriv, 2) - 1;
  derivBlock(:, colStart:colEnd) = currentDeriv;
  colStart = colEnd + 1;
end
end


function [pilotFd, dPilotStruct] = localBuildPilotStructJacobian( ...
  pilotVec, timeLoc, tauFrame, fdSat, fdRateSat, dFdSat, dFdRate, phaseMode)
%LOCALBUILDPILOTSTRUCTJACOBIAN Build pilot template and structural derivatives.

numSample = numel(pilotVec);
numStruct = numel(dFdSat);
pilotVec = reshape(pilotVec, 1, []);
timeLoc = reshape(timeLoc, 1, []);

switch phaseMode
  case {'continuous', 'relaxed'}
    timeUse = tauFrame + timeLoc;
    phaseArg = 2 * pi * (fdSat * timeUse + 0.5 * fdRateSat * (timeUse .^ 2));
    pilotFd = pilotVec .* exp(1j * phaseArg);

    dPilotStruct = zeros(numSample, numStruct);
    for iParam = 1:numStruct
      phaseJac = 2 * pi * (dFdSat(iParam) * timeUse + ...
        0.5 * dFdRate(iParam) * (timeUse .^ 2));
      dPilotStruct(:, iParam) = (1j * phaseJac .* pilotFd).';
    end

  case 'independent'
    phaseArg = 2 * pi * ((fdSat + fdRateSat * tauFrame) * timeLoc + ...
      0.5 * fdRateSat * (timeLoc .^ 2));
    pilotFd = pilotVec .* exp(1j * phaseArg);

    dPilotStruct = zeros(numSample, numStruct);
    for iParam = 1:numStruct
      phaseJac = 2 * pi * ((dFdSat(iParam) + dFdRate(iParam) * tauFrame) * timeLoc + ...
        0.5 * dFdRate(iParam) * (timeLoc .^ 2));
      dPilotStruct(:, iParam) = (1j * phaseJac .* pilotFd).';
    end

  otherwise
    error('crbPilotMfDoaDoppler:InvalidPhaseMode', ...
      'Unsupported phase mode: %s.', phaseMode);
end

end


function [fimInterest, crbInterest, crbFull] = localExtractInterestCrb(fimFull, interestIdx, nuisanceIdx)
%LOCALEXTRACTINTERESTCRB Extract interest-parameter CRB from the full FIM.

if isempty(nuisanceIdx)
  fimInterest = fimFull(interestIdx, interestIdx);
else
  fimTheta = fimFull(interestIdx, interestIdx);
  fimCross = fimFull(interestIdx, nuisanceIdx);
  fimNuis = fimFull(nuisanceIdx, nuisanceIdx);

  fimInterest = fimTheta - fimCross * localSafeSolve(fimNuis, fimCross.');
  fimInterest = 0.5 * (fimInterest + fimInterest.');
end

crbInterest = localSafeInverse(fimInterest, 'crbPilotMfDoaDoppler:IllConditionedInterestFim', ...
  'The interest-parameter FIM is singular or ill-conditioned. Using pinv.');
crbFull = localSafeInverse(fimFull, 'crbPilotMfDoaDoppler:IllConditionedFullFim', ...
  'The full FIM is singular or ill-conditioned. Using pinv.');
end


function x = localSafeSolve(aMat, bMat)
%LOCALSAFESOLVE Solve a linear system with a pinv fallback.

aMat = 0.5 * (aMat + aMat.');
if isempty(aMat)
  x = zeros(size(aMat, 1), size(bMat, 2));
  return;
end

if rcond(aMat + eye(size(aMat)) * eps) < 1e-12
  x = pinv(aMat) * bMat;
else
  x = aMat \ bMat;
end
end


function invMat = localSafeInverse(aMat, warnId, warnMsg)
%LOCALSAFEINVERSE Invert a symmetric matrix with a pinv fallback.

aMat = 0.5 * (aMat + aMat.');
if rcond(aMat + eye(size(aMat)) * eps) < 1e-12
  warning(warnId, '%s', warnMsg);
  invMat = pinv(aMat);
else
  invMat = aMat \ eye(size(aMat));
end
invMat = 0.5 * (invMat + invMat.');
end


function modelType = localGetModelType(phaseMode, fdRateMode)
%LOCALGETMODELTYPE Get the compact model tag used by scripts.

switch phaseMode
  case 'continuous'
    phaseTag = 'mfCp';
  case 'relaxed'
