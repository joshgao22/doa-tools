  case 'globalmin'
    obsStartTime = min(tauGeom(:));

  case 'obsstart'
    if isempty(obsStartTime)
      error('genMultiFrameSnapshots:MissingObsStartTime', ...
        'obsStartTime is required when the time reference is custom.');
    end

  case 'total'
    obsStartTime = 0;

  otherwise
    error('genMultiFrameSnapshots:UnsupportedDelayRefMode', ...
      'Unsupported delayRefMode: %s.', delayRefMode);
end

tauEff = tauGeom - obsStartTime;
end


function delayPad = localBuildDelayPadFrame(delaySamples, delayMode, outputLengthMode)
%LOCALBUILDDELAYPADFRAME Build padding length for one frame.

delayPad = 0;

if ~strcmp(delayMode, 'waveform') || ~strcmp(outputLengthMode, 'globalexpand')
  return;
end

minDelay = min(delaySamples(:));
if minDelay < 0
  error('genMultiFrameSnapshots:NegativeEffectiveDelay', ...
    ['Effective delay is negative under the selected global reference. ', ...
     'Please move obsStartTime earlier or use timeRef=''minDelay''.']);
end

delayPad = ceil(max(delaySamples(:)));
end


function phaseVec = localEvalContinuousDopplerPhase(anchorSec, fdVec, queryTimeSec)
%LOCALEVALCONTINUOUSDOPPLERPHASE Evaluate continuous phase from frame-wise
% Doppler samples using piecewise-linear interpolation.

anchorSec = reshape(anchorSec, [], 1);
fdVec = reshape(fdVec, [], 1);
queryTimeSec = reshape(queryTimeSec, 1, []);

if numel(anchorSec) ~= numel(fdVec)
  error('genMultiFrameSnapshots:AnchorFdSizeMismatch', ...
    'anchorSec and fdVec must have the same number of entries.');
end

if numel(anchorSec) > 1 && any(diff(anchorSec) <= 0)
  error('genMultiFrameSnapshots:NonIncreasingAnchorSec', ...
    'Absolute frame anchor times must be strictly increasing.');
end

numAnchor = numel(anchorSec);
phaseAnchor = zeros(numAnchor, 1);

for iAnchor = 2:numAnchor
  deltaTime = anchorSec(iAnchor) - anchorSec(iAnchor - 1);
  phaseAnchor(iAnchor) = phaseAnchor(iAnchor - 1) + ...
    2*pi * 0.5 * (fdVec(iAnchor - 1) + fdVec(iAnchor)) * deltaTime;
end

phaseVec = zeros(1, numel(queryTimeSec));

if numAnchor == 1
  phaseVec = 2*pi * fdVec(1) * (queryTimeSec - anchorSec(1));
  return;
end

for iSample = 1:numel(queryTimeSec)
  currentTime = queryTimeSec(iSample);

  if currentTime <= anchorSec(1)
    phaseVec(iSample) = phaseAnchor(1) + ...
      2*pi * fdVec(1) * (currentTime - anchorSec(1));
    continue;
  end

  if currentTime >= anchorSec(end)
    phaseVec(iSample) = phaseAnchor(end) + ...
      2*pi * fdVec(end) * (currentTime - anchorSec(end));
    continue;
  end

  leftIdx = find(anchorSec <= currentTime, 1, 'last');
  rightIdx = leftIdx + 1;

  leftTime = anchorSec(leftIdx);
  rightTime = anchorSec(rightIdx);
  leftFd = fdVec(leftIdx);
  rightFd = fdVec(rightIdx);

  deltaTime = rightTime - leftTime;
  relTime = currentTime - leftTime;
  fdSlope = (rightFd - leftFd) / deltaTime;

  phaseVec(iSample) = phaseAnchor(leftIdx) + ...
    2*pi * (leftFd * relTime + 0.5 * fdSlope * relTime^2);
end
end


function phaseMat = localParseStaticPhaseSpec(phaseInput, numSat, numUser, framePhaseModel, targetMode)
%LOCALPARSESTATICPHASESPEC Parse static phase specification in radians.

if ~strcmp(framePhaseModel, targetMode)
  phaseMat = zeros(numSat, numUser);
  return;
end

if isempty(phaseInput)
  phaseMat = 2*pi * rand(numSat, numUser);
  return;
end

phaseMat = localExpandStaticMatrix(phaseInput, numSat, numUser, 'phase');
end


function framePhaseCell = localParseFramePhaseSpec(sharedPhaseMat, phaseInput, numSat, numUser, numFrame, framePhaseModel)
%LOCALPARSEFRAMEPHASESPEC Parse frame-wise phase specification in radians.

framePhaseCell = repmat({zeros(numSat, numUser)}, 1, numFrame);

switch framePhaseModel
  case 'none'
    return;

  case 'shared'
    framePhaseCell = repmat({sharedPhaseMat}, 1, numFrame);
    return;

  case {'independent', 'custom'}
    % keep parsing below

  otherwise
    error('genMultiFrameSnapshots:UnsupportedFramePhaseModel', ...
      'Unsupported framePhaseModel: %s.', framePhaseModel);
end

if isempty(phaseInput)
  if strcmp(framePhaseModel, 'independent')
    for iFrame = 1:numFrame
      framePhaseCell{iFrame} = 2*pi * rand(numSat, numUser);
    end
    return;
  end

  error('genMultiFrameSnapshots:MissingFramePhase', ...
    'A frame phase specification is required for frameModel=''custom''.');
end

if iscell(phaseInput)
  if numel(phaseInput) ~= numFrame
    error('genMultiFrameSnapshots:InvalidFramePhaseCellSize', ...
      'framePhase cell input must contain one entry per frame.');
  end

  framePhaseCell = reshape(phaseInput, 1, []);
  for iFrame = 1:numFrame
    framePhaseCell{iFrame} = localExpandStaticMatrix( ...
      framePhaseCell{iFrame}, numSat, numUser, 'frame phase');
  end
  return;
end

if isnumeric(phaseInput) && ndims(phaseInput) == 3 && ...
    isequal(size(phaseInput), [numSat, numUser, numFrame])
  for iFrame = 1:numFrame
    framePhaseCell{iFrame} = phaseInput(:, :, iFrame);
  end
  return;
end

if isnumeric(phaseInput) && ismatrix(phaseInput)
  currentPhaseMat = localExpandStaticMatrix(phaseInput, numSat, numUser, 'frame phase');
  framePhaseCell = repmat({currentPhaseMat}, 1, numFrame);
  return;
end

error('genMultiFrameSnapshots:InvalidFramePhase', ...
  ['framePhase must be a scalar, vector, numSat x numUser matrix, ', ...
   'numSat x numUser x numFrame array, or a 1xNf cell array.']);
end


function matOut = localExpandStaticMatrix(matIn, numRow, numCol, argName)
%LOCALEXPANDSTATICMATRIX Expand scalar/vector/matrix into numRow x numCol.

if ~isnumeric(matIn) || any(~isfinite(matIn(:)))
  error('genMultiFrameSnapshots:InvalidNumericSpec', ...
    '%s must be numeric and finite.', argName);
end

if isscalar(matIn)
  matOut = repmat(matIn, numRow, numCol);
  return;
end

if isvector(matIn)
  vecIn = matIn(:);

  if numel(vecIn) == numCol
    matOut = repmat(vecIn.', numRow, 1);
    return;
  end

  if numel(vecIn) == numRow
    matOut = repmat(vecIn, 1, numCol);
    return;
  end

  error('genMultiFrameSnapshots:InvalidVectorLength', ...
    '%s vector length must match numUser or numSat.', argName);
end

if isequal(size(matIn), [numRow, numCol])
  matOut = matIn;
  return;
end

error('genMultiFrameSnapshots:InvalidMatrixSize', ...
  '%s must be scalar, vector, or numSat x numUser matrix.', argName);
end


