function [rxSig, sampleCov, cleanSig, noiseSig, meta] = genMultiFrameSnapshots( ...
  sceneSeq, txSig, carrierFreq, sampleRate, noiseCov, pathGain, modelOpt)
%GENMULTIFRAMESNAPSHOTS Generate multi-frame multi-satellite snapshots.
% This wrapper supports both a simplified grouped interface and the legacy
% flat options. It repeatedly calls genMultiSatSnapshots on one frame at a
% time, while providing a clearer control of:
%   1) spatial model across frames
%   2) phase-time model across frames
%   3) extra frame phase model
%   4) waveform propagation options
%
%Syntax:
%  [rxSig, sampleCov, cleanSig, noiseSig, meta] = genMultiFrameSnapshots( ...
%    sceneSeq, txSig, carrierFreq, sampleRate)
%
%  ... = genMultiFrameSnapshots( ...
%    sceneSeq, txSig, carrierFreq, sampleRate, noiseCov)
%
%  ... = genMultiFrameSnapshots( ...
%    sceneSeq, txSig, carrierFreq, sampleRate, noiseCov, pathGain)
%
%  ... = genMultiFrameSnapshots( ...
%    sceneSeq, txSig, carrierFreq, sampleRate, noiseCov, pathGain, modelOpt)
%
%Inputs:
%  sceneSeq         - multi-frame scene structure generated by
%                     genMultiFrameScene
%
%  txSig            - transmitted baseband waveform specification
%                     - Nu x T matrix : shared waveform for all frames
%                     - 1xNf cell     : one Nu x Tn waveform matrix per frame
%
%  carrierFreq      - carrier frequency in Hz
%
%  sampleRate       - waveform sampling rate in Hz
%
%  noiseCov         - (optional) shared or frame-wise noise covariance
%                     specification accepted by genMultiSatSnapshots
%                     default: 1
%
%  pathGain         - (optional) shared or frame-wise complex path gain
%                     specification accepted by genMultiSatSnapshots
%                     default: 1
%
%  modelOpt         - optional generation options. Recommended grouped form:
%
%                     .lightSpeed           - propagation speed in m/s
%                                             default: 299792458
%
%                     .spatial.model        - cross-frame spatial model
%                                             - 'dynamic' : use each frame
%                                                           local DoA/access
%                                             - 'frozen'  : freeze both to a
%                                                           reference frame
%                                             - 'custom'  : use precomputed
%                                                           steeringInputCell
%                                             default: 'dynamic'
%                     .spatial.refFrameIdx  - reference frame used when
%                                             spatial.model='frozen'
%                                             default: sceneSeq.refFrameIdx
%
%                     .phase.timeModel      - phase-time model
%                                             - 'global'     : absolute-time
%                                                              continuous phase
%                                                              from frame-wise
%                                                              Doppler samples
%                                             - 'frameLocal' : phase restarts
%                                                              at each frame
%                                             - 'linearRef'  : absolute-time
%                                                              affine Doppler
%                                                              fitted from the
%                                                              reference chain
%                                             - 'custom'     : use precomputed
%                                                              phaseSeqCell
%                                             default: 'global'
%                     .phase.frameModel     - extra frame phase model
%                                             - 'none'        : no extra phase
%                                             - 'shared'      : one constant
%                                                               phase per link
%                                                               shared by all
%                                                               frames
%                                             - 'independent' : one phase per
%                                                               frame/link
%                                             - 'custom'      : use provided
%                                                               phase matrices
%                                             default: 'none'
%                     .phase.sharedPhase    - optional shared phase spec for
%                                             frameModel='shared'
%                     .phase.framePhase     - optional frame-wise phase spec
%                                             for frameModel='independent' or
%                                             'custom'
%
%                     .wave.delayModel      - 'waveform' or 'phaseOnly'
%                                             default: 'waveform'
%                     .wave.timeRef         - waveform time reference
%                                             - 'minDelay' : subtract the
%                                                            earliest delay
%                                             - 'zero'     : use total delay
%                                             - 'custom'   : use obsStartTime
%                                             default: 'minDelay'
%                     .wave.obsStartTime    - observation start time used when
%                                             wave.timeRef='custom'
%                     .wave.carrierPhaseModel
%                                           - 'relativeDelay',
%                                             'absoluteDelay', or 'none'
%                                             default: 'relativeDelay'
%                     .wave.rxLengthMode    - 'expand' or 'same'
%                                             default: 'expand'
%                     .wave.filterHalfLen   - default: 8
%                     .wave.windowType      - default: 'hann'
%                     .wave.useAccessMask   - default: true
%
%                     .precomp.steeringInputCell
%                                           - optional precomputed 1xNf cell
%                     .precomp.linkParamCell
%                                           - optional precomputed 1xNf cell
%                     .precomp.phaseSeqCell - optional precomputed 1xNf cell,
%                                             each entry is a 1xNs cell of
%                                             Nu x Nrx complex phase sequences
%
%                     .advanced.snapshotModelOpt
%                                           - optional legacy per-frame opts
%                                             forwarded to genMultiSatSnapshots
%
%                     Legacy flat fields are still accepted for backward
%                     compatibility:
%                       steeringMode, accessMode, steeringRefFrameIdx,
%                       dopplerMode, steeringInputCell, linkParamCell,
%                       snapshotModelOpt.
%
%Outputs:
%  rxSig            - 1xNf cell, each cell is the rxSig output of one frame
%
%  sampleCov        - 1xNf cell, each cell is the sampleCov output of one
%                     frame
%
%  cleanSig         - 1xNf cell, each cell is the cleanSig output of one
%                     frame
%
%  noiseSig         - 1xNf cell, each cell is the noiseSig output of one
%                     frame
%
%  meta             - auxiliary information
%
%Notes:
%  - The output layout is unchanged from the previous version, so existing
%    estimators do not need to be modified.
%  - The new grouped options only simplify the public interface. Internally
%    they are mapped back to genMultiSatSnapshots.
%
%See also:
%  genMultiFrameScene, genMultiSatSnapshots, getSceneSteering, getLinkParam

arguments
  sceneSeq (1,1) struct
  txSig
  carrierFreq (1,1) {mustBePositive, mustBeNumeric, mustBeFinite}
  sampleRate (1,1) {mustBePositive, mustBeNumeric, mustBeFinite}
  noiseCov = 1
  pathGain = 1
  modelOpt (1,1) struct = struct()
end

% -------------------------------------------------------------------------
% Basic scene-sequence checks
% -------------------------------------------------------------------------
localCheckSceneSeq(sceneSeq);

numFrame = sceneSeq.numFrame;
numSat = sceneSeq.numSat;
numUser = sceneSeq.numUser;

% -------------------------------------------------------------------------
% Parse generation options
% -------------------------------------------------------------------------
genOpt = localParseGenOpt(modelOpt, sceneSeq, numSat, numUser, numFrame);
lightSpeed = genOpt.lightSpeed;
carrierWavelen = lightSpeed / carrierFreq;

% -------------------------------------------------------------------------
% Parse per-frame inputs
% -------------------------------------------------------------------------
[txSigCell, txShared] = localParseTxSig(txSig, numFrame, numUser);
noiseCovCell = localExpandFrameInput(noiseCov, numFrame, 'noiseCov');
pathGainCell = localExpandFrameInput(pathGain, numFrame, 'pathGain');
frameModelOptCell = localBuildFrameModelOptCell(genOpt, numFrame);

% -------------------------------------------------------------------------
% Build / parse per-frame steering input
% -------------------------------------------------------------------------
if isempty(genOpt.steeringInputCell)
  steeringInputCell = localBuildSteeringInputCell( ...
    sceneSeq, carrierWavelen, genOpt.steeringMode, ...
    genOpt.accessMode, genOpt.steeringRefFrameIdx);
else
  steeringInputCell = localParseSteeringInputCell(genOpt.steeringInputCell, numFrame);
end

% -------------------------------------------------------------------------
% Build / parse per-frame link parameters
% -------------------------------------------------------------------------
if isempty(genOpt.linkParamCell)
  linkParamCell = cell(1, numFrame);
  for iFrame = 1:numFrame
    linkParamCell{iFrame} = getLinkParam(sceneSeq.sceneCell{iFrame}, carrierWavelen, lightSpeed);
  end
else
  linkParamCell = localParseLinkParamCell(genOpt.linkParamCell, numFrame);
end

% -------------------------------------------------------------------------
% Build / parse per-frame phase sequences
% -------------------------------------------------------------------------
phaseSeqCell = localBuildFramePhaseSeqCell( ...
  sceneSeq, txSigCell, linkParamCell, sampleRate, frameModelOptCell, genOpt);

for iFrame = 1:numFrame
  if isfield(frameModelOptCell{iFrame}, 'dopplerSeqCell') && ...
      ~isempty(frameModelOptCell{iFrame}.dopplerSeqCell)
    error('genMultiFrameSnapshots:DuplicatePhaseSeqCell', ...
      ['The forwarded frame model already contains dopplerSeqCell. ', ...
       'Please remove it from advanced.snapshotModelOpt or use ', ...
       'phase.timeModel=''custom'' through precomp.phaseSeqCell only.']);
  end

  frameModelOptCell{iFrame}.dopplerSeqCell = phaseSeqCell{iFrame};
end

% -------------------------------------------------------------------------
% Initialize outputs
% -------------------------------------------------------------------------
rxSig = cell(1, numFrame);
sampleCov = cell(1, numFrame);
cleanSig = cell(1, numFrame);
noiseSig = cell(1, numFrame);
frameMeta = cell(1, numFrame);

% -------------------------------------------------------------------------
% Generate one frame at a time
% -------------------------------------------------------------------------
for iFrame = 1:numFrame
  [rxSig{iFrame}, sampleCov{iFrame}, cleanSig{iFrame}, noiseSig{iFrame}, frameMeta{iFrame}] = ...
    genMultiSatSnapshots( ...
      steeringInputCell{iFrame}, ...
      txSigCell{iFrame}, ...
      linkParamCell{iFrame}, ...
      carrierFreq, ...
      sampleRate, ...
      noiseCovCell{iFrame}, ...
      pathGainCell{iFrame}, ...
      frameModelOptCell{iFrame});

  localCheckFrameOutput(rxSig{iFrame}, sampleCov{iFrame}, cleanSig{iFrame}, ...
    noiseSig{iFrame}, frameMeta{iFrame}, numSat, numUser, iFrame);
end

% -------------------------------------------------------------------------
% Pack outputs
% -------------------------------------------------------------------------
meta = struct();
meta.numFrame = numFrame;
meta.numSat = numSat;
meta.numUser = numUser;

meta.utcVec = sceneSeq.utcVec;
meta.utcRef = sceneSeq.utcRef;
meta.refFrameIdx = sceneSeq.refFrameIdx;
meta.timeOffsetSec = sceneSeq.timeOffsetSec;

meta.carrierFreq = carrierFreq;
meta.carrierWavelen = carrierWavelen;
meta.sampleRate = sampleRate;
meta.lightSpeed = lightSpeed;

meta.spatialModel = genOpt.spatialModel;
meta.phaseTimeModel = genOpt.phaseTimeModel;
meta.framePhaseModel = genOpt.framePhaseModel;
meta.steeringMode = genOpt.steeringMode;
meta.accessMode = genOpt.accessMode;
meta.steeringRefFrameIdx = genOpt.steeringRefFrameIdx;
if strcmp(genOpt.phaseTimeModel, 'global')
  meta.dopplerMode = 'continuous';
elseif strcmp(genOpt.phaseTimeModel, 'framelocal')
  meta.dopplerMode = 'frameLocal';
elseif strcmp(genOpt.phaseTimeModel, 'linearref')
  meta.dopplerMode = 'linearRef';
else
  meta.dopplerMode = 'custom';
end

meta.txShared = txShared;
meta.txSigCell = txSigCell;
meta.steeringInputCell = steeringInputCell;
meta.linkParamCell = linkParamCell;
meta.phaseSeqCell = phaseSeqCell;
meta.frameModelOptCell = frameModelOptCell;
meta.snapshotModelOptCell = frameModelOptCell;
meta.frameMeta = frameMeta;
end


function localCheckSceneSeq(sceneSeq)
%LOCALCHECKSCENESEQ Check basic validity of sceneSeq.

requiredField = {'numFrame', 'numSat', 'numUser', 'sceneCell', 'utcVec', ...
  'utcRef', 'refFrameIdx', 'timeOffsetSec'};

for iField = 1:numel(requiredField)
  if ~isfield(sceneSeq, requiredField{iField})
    error('genMultiFrameSnapshots:MissingSceneSeqField', ...
      'sceneSeq.%s is required.', requiredField{iField});
  end
end

if ~isscalar(sceneSeq.numFrame) || sceneSeq.numFrame < 1 || mod(sceneSeq.numFrame, 1) ~= 0
  error('genMultiFrameSnapshots:InvalidNumFrame', ...
    'sceneSeq.numFrame must be a positive integer scalar.');
end

if ~iscell(sceneSeq.sceneCell) || numel(sceneSeq.sceneCell) ~= sceneSeq.numFrame
  error('genMultiFrameSnapshots:InvalidSceneCell', ...
    'sceneSeq.sceneCell must be a cell array with one entry per frame.');
end

if ~isvector(sceneSeq.utcVec) || numel(sceneSeq.utcVec) ~= sceneSeq.numFrame
  error('genMultiFrameSnapshots:InvalidUtcVec', ...
    'sceneSeq.utcVec must contain one epoch per frame.');
end

if ~isvector(sceneSeq.timeOffsetSec) || numel(sceneSeq.timeOffsetSec) ~= sceneSeq.numFrame
  error('genMultiFrameSnapshots:InvalidTimeOffset', ...
    'sceneSeq.timeOffsetSec must contain one value per frame.');
end
end


function genOpt = localParseGenOpt(modelOpt, sceneSeq, numSat, numUser, numFrame)
%LOCALPARSEGENOPT Parse grouped and legacy generation options.

if ~isstruct(modelOpt)
  error('genMultiFrameSnapshots:InvalidModelOpt', ...
    'modelOpt must be a struct.');
end

genOpt = struct();

% -----------------------------------------------------------------------
% Common scalar options
% -----------------------------------------------------------------------
genOpt.lightSpeed = localReadField(modelOpt, {'lightSpeed'}, 299792458);
if ~isscalar(genOpt.lightSpeed) || ~isfinite(genOpt.lightSpeed) || genOpt.lightSpeed <= 0
  error('genMultiFrameSnapshots:InvalidLightSpeed', ...
    'lightSpeed must be a positive finite scalar.');
end

% -----------------------------------------------------------------------
% Spatial options
% -----------------------------------------------------------------------
spatialOpt = localReadField(modelOpt, {'spatial'}, struct());
if ~isstruct(spatialOpt)
  error('genMultiFrameSnapshots:InvalidSpatialOpt', ...
    'modelOpt.spatial must be a struct.');
end

spatialModel = localReadField(spatialOpt, {'model'}, '');
if isempty(spatialModel)
  steeringModeLegacy = localReadField(modelOpt, {'steeringMode'}, 'framewise');
  accessModeLegacy = localReadField(modelOpt, {'accessMode'}, 'framewise');

  steeringModeLegacy = localNormalizeText(steeringModeLegacy, 'framewise');
  accessModeLegacy = localNormalizeText(accessModeLegacy, 'framewise');

  if strcmp(steeringModeLegacy, 'framewise') && strcmp(accessModeLegacy, 'framewise')
    spatialModel = 'dynamic';
  elseif strcmp(steeringModeLegacy, 'frozenref') && strcmp(accessModeLegacy, 'frozenref')
    spatialModel = 'frozen';
  elseif isfield(modelOpt, 'steeringInputCell') && ~isempty(modelOpt.steeringInputCell)
    spatialModel = 'custom';
  else
    spatialModel = 'legacy';
  end
else
  spatialModel = localNormalizeText(spatialModel, 'dynamic');
end

genOpt.spatialModel = spatialModel;

switch spatialModel
  case 'dynamic'
    genOpt.steeringMode = 'framewise';
    genOpt.accessMode = 'framewise';

  case 'frozen'
    genOpt.steeringMode = 'frozenref';
    genOpt.accessMode = 'frozenref';

  case 'custom'
    genOpt.steeringMode = 'custom';
    genOpt.accessMode = 'custom';

  case 'legacy'
    genOpt.steeringMode = localNormalizeText(localReadField(modelOpt, {'steeringMode'}, 'framewise'), 'framewise');
    genOpt.accessMode = localNormalizeText(localReadField(modelOpt, {'accessMode'}, 'framewise'), 'framewise');

  otherwise
    error('genMultiFrameSnapshots:UnsupportedSpatialModel', ...
      'Unsupported spatial.model: %s.', spatialModel);
end

validLegacyMode = {'framewise', 'frozenref', 'custom'};
if ~ismember(genOpt.steeringMode, validLegacyMode)
  error('genMultiFrameSnapshots:UnsupportedSteeringMode', ...
    'Unsupported steering mode: %s.', genOpt.steeringMode);
end

if ~ismember(genOpt.accessMode, validLegacyMode)
  error('genMultiFrameSnapshots:UnsupportedAccessMode', ...
    'Unsupported access mode: %s.', genOpt.accessMode);
end

genOpt.steeringRefFrameIdx = localReadField(spatialOpt, {'refFrameIdx'}, []);
if isempty(genOpt.steeringRefFrameIdx)
  genOpt.steeringRefFrameIdx = localReadField(modelOpt, {'steeringRefFrameIdx'}, sceneSeq.refFrameIdx);
end

if ~isscalar(genOpt.steeringRefFrameIdx) || ~isfinite(genOpt.steeringRefFrameIdx) || ...
    mod(genOpt.steeringRefFrameIdx, 1) ~= 0 || ...
    genOpt.steeringRefFrameIdx < 1 || genOpt.steeringRefFrameIdx > sceneSeq.numFrame
  error('genMultiFrameSnapshots:InvalidSteeringRefFrameIdx', ...
    'The reference frame index must be an integer within [1, numFrame].');
end

% -----------------------------------------------------------------------
% Phase options
% -----------------------------------------------------------------------
phaseOpt = localReadField(modelOpt, {'phase'}, struct());
if ~isstruct(phaseOpt)
  error('genMultiFrameSnapshots:InvalidPhaseOpt', ...
    'modelOpt.phase must be a struct.');
end

phaseTimeModel = localReadField(phaseOpt, {'timeModel'}, '');
if isempty(phaseTimeModel)
  dopplerModeLegacy = localReadField(modelOpt, {'dopplerMode'}, 'continuous');
  dopplerModeLegacy = localNormalizeText(dopplerModeLegacy, 'continuous');

  switch dopplerModeLegacy
    case 'continuous'
      phaseTimeModel = 'global';
    case 'framelocal'
      phaseTimeModel = 'framelocal';
    otherwise
      error('genMultiFrameSnapshots:UnsupportedDopplerMode', ...
        'Unsupported legacy dopplerMode: %s.', dopplerModeLegacy);
  end
else
  phaseTimeModel = localNormalizeText(phaseTimeModel, 'global');
end

genOpt.phaseTimeModel = phaseTimeModel;
validPhaseTimeModel = {'global', 'framelocal', 'linearref', 'custom'};
if ~ismember(genOpt.phaseTimeModel, validPhaseTimeModel)
  error('genMultiFrameSnapshots:UnsupportedPhaseTimeModel', ...
    'Unsupported phase.timeModel: %s.', genOpt.phaseTimeModel);
end

framePhaseModel = localReadField(phaseOpt, {'frameModel'}, 'none');
framePhaseModel = localNormalizeText(framePhaseModel, 'none');
genOpt.framePhaseModel = framePhaseModel;
validFramePhaseModel = {'none', 'shared', 'independent', 'custom'};
if ~ismember(genOpt.framePhaseModel, validFramePhaseModel)
  error('genMultiFrameSnapshots:UnsupportedFramePhaseModel', ...
    'Unsupported phase.frameModel: %s.', genOpt.framePhaseModel);
end

precompOpt = localReadField(modelOpt, {'precomp'}, struct());
if ~isstruct(precompOpt)
  error('genMultiFrameSnapshots:InvalidPrecompOpt', ...
    'modelOpt.precomp must be a struct.');
end

if isfield(precompOpt, 'steeringInputCell') && ~isempty(precompOpt.steeringInputCell)
  genOpt.steeringInputCell = precompOpt.steeringInputCell;
elseif isfield(modelOpt, 'steeringInputCell') && ~isempty(modelOpt.steeringInputCell)
  genOpt.steeringInputCell = modelOpt.steeringInputCell;
else
  genOpt.steeringInputCell = [];
end

if isfield(precompOpt, 'linkParamCell') && ~isempty(precompOpt.linkParamCell)
  genOpt.linkParamCell = precompOpt.linkParamCell;
elseif isfield(modelOpt, 'linkParamCell') && ~isempty(modelOpt.linkParamCell)
  genOpt.linkParamCell = modelOpt.linkParamCell;
else
  genOpt.linkParamCell = [];
end

if isfield(precompOpt, 'phaseSeqCell') && ~isempty(precompOpt.phaseSeqCell)
  genOpt.phaseSeqCell = precompOpt.phaseSeqCell;
elseif isfield(phaseOpt, 'phaseSeqCell') && ~isempty(phaseOpt.phaseSeqCell)
  genOpt.phaseSeqCell = phaseOpt.phaseSeqCell;
else
  genOpt.phaseSeqCell = [];
end

if strcmp(genOpt.phaseTimeModel, 'custom') && isempty(genOpt.phaseSeqCell)
  error('genMultiFrameSnapshots:MissingPhaseSeqCell', ...
    'phase.timeModel=''custom'' requires precomp.phaseSeqCell or phase.phaseSeqCell.');
end

sharedPhaseInput = [];
if isfield(phaseOpt, 'sharedPhase') && ~isempty(phaseOpt.sharedPhase)
  sharedPhaseInput = phaseOpt.sharedPhase;
elseif isfield(phaseOpt, 'sharedPhaseMat') && ~isempty(phaseOpt.sharedPhaseMat)
  sharedPhaseInput = phaseOpt.sharedPhaseMat;
end

genOpt.sharedPhaseMat = localParseStaticPhaseSpec( ...
  sharedPhaseInput, numSat, numUser, genOpt.framePhaseModel, 'shared');

framePhaseInput = [];
if isfield(phaseOpt, 'framePhase') && ~isempty(phaseOpt.framePhase)
  framePhaseInput = phaseOpt.framePhase;
elseif isfield(phaseOpt, 'framePhaseMat') && ~isempty(phaseOpt.framePhaseMat)
  framePhaseInput = phaseOpt.framePhaseMat;
elseif isfield(phaseOpt, 'framePhaseCell') && ~isempty(phaseOpt.framePhaseCell)
  framePhaseInput = phaseOpt.framePhaseCell;
end

genOpt.framePhaseCell = localParseFramePhaseSpec( ...
  genOpt.sharedPhaseMat, framePhaseInput, numSat, numUser, numFrame, genOpt.framePhaseModel);

% -----------------------------------------------------------------------
% Wave options + advanced legacy forward options
% -----------------------------------------------------------------------
advancedOpt = localReadField(modelOpt, {'advanced'}, struct());
if ~isstruct(advancedOpt)
  error('genMultiFrameSnapshots:InvalidAdvancedOpt', ...
    'modelOpt.advanced must be a struct.');
end

if isfield(advancedOpt, 'snapshotModelOpt') && ~isempty(advancedOpt.snapshotModelOpt)
  snapshotModelOptIn = advancedOpt.snapshotModelOpt;
elseif isfield(modelOpt, 'snapshotModelOpt') && ~isempty(modelOpt.snapshotModelOpt)
  snapshotModelOptIn = modelOpt.snapshotModelOpt;
else
  snapshotModelOptIn = struct();
end

genOpt.snapshotModelOpt = snapshotModelOptIn;

genOpt.wave = localParseWaveOpt(localReadField(modelOpt, {'wave'}, struct()));
end


function waveOpt = localParseWaveOpt(waveOptIn)
%LOCALPARSEWAVEOPT Parse grouped wave options.

if isempty(waveOptIn)
  waveOptIn = struct();
end

if ~isstruct(waveOptIn)
  error('genMultiFrameSnapshots:InvalidWaveOpt', ...
    'modelOpt.wave must be a struct.');
end

waveOpt = struct();
waveOpt.delayMode = localNormalizeText(localReadField(waveOptIn, {'delayModel'}, 'waveform'), 'waveform');
waveTimeRef = localNormalizeText(localReadField(waveOptIn, {'timeRef'}, 'mindelay'), 'mindelay');
waveOpt.carrierPhaseModel = localNormalizeText(localReadField(waveOptIn, {'carrierPhaseModel'}, 'relativedelay'), 'relativedelay');
waveOpt.rxLengthMode = localNormalizeText(localReadField(waveOptIn, {'rxLengthMode'}, 'expand'), 'expand');
waveOpt.obsStartTime = localReadField(waveOptIn, {'obsStartTime'}, []);
waveOpt.filterHalfLen = localReadField(waveOptIn, {'filterHalfLen'}, 8);
waveOpt.windowType = localReadField(waveOptIn, {'windowType'}, 'hann');
waveOpt.useAccessMask = localReadField(waveOptIn, {'useAccessMask'}, true);

validDelayModel = {'waveform', 'phaseonly'};
if ~ismember(waveOpt.delayMode, validDelayModel)
  error('genMultiFrameSnapshots:UnsupportedDelayModel', ...
    'Unsupported wave.delayModel: %s.', waveOpt.delayMode);
end

switch waveTimeRef
  case {'mindelay', 'globalmin'}
    waveOpt.delayRefMode = 'globalMin';
  case {'zero', 'total'}
    waveOpt.delayRefMode = 'total';
  case {'custom', 'obsstart'}
    waveOpt.delayRefMode = 'obsStart';
  otherwise
    error('genMultiFrameSnapshots:UnsupportedTimeRef', ...
      'Unsupported wave.timeRef: %s.', waveTimeRef);
end

switch waveOpt.carrierPhaseModel
  case {'relativedelay', 'effective'}
    waveOpt.carrierPhaseMode = 'effective';
  case {'absolutedelay', 'total'}
    waveOpt.carrierPhaseMode = 'total';
  case 'none'
    waveOpt.carrierPhaseMode = 'none';
  otherwise
    error('genMultiFrameSnapshots:UnsupportedCarrierPhaseModel', ...
      'Unsupported wave.carrierPhaseModel: %s.', waveOpt.carrierPhaseModel);
end

switch waveOpt.rxLengthMode
  case {'expand', 'globalexpand'}
    waveOpt.outputLengthMode = 'globalExpand';
  case 'same'
    waveOpt.outputLengthMode = 'same';
  otherwise
    error('genMultiFrameSnapshots:UnsupportedRxLengthMode', ...
      'Unsupported wave.rxLengthMode: %s.', waveOpt.rxLengthMode);
end

if ~isscalar(waveOpt.filterHalfLen) || ~isfinite(waveOpt.filterHalfLen) || ...
    waveOpt.filterHalfLen <= 0 || mod(waveOpt.filterHalfLen, 1) ~= 0
  error('genMultiFrameSnapshots:InvalidFilterHalfLen', ...
    'wave.filterHalfLen must be a positive integer scalar.');
end

if isstring(waveOpt.windowType)
  waveOpt.windowType = char(waveOpt.windowType);
end

if ~ischar(waveOpt.windowType)
  error('genMultiFrameSnapshots:InvalidWindowType', ...
    'wave.windowType must be a character vector or string scalar.');
end

waveOpt.useAccessMask = logical(waveOpt.useAccessMask);
end


function frameModelOptCell = localBuildFrameModelOptCell(genOpt, numFrame)
%LOCALBUILDFRAMEMODELOPTCELL Build per-frame forwarded options.

frameModelOptCell = localParseSnapshotModelOpt(genOpt.snapshotModelOpt, numFrame);

for iFrame = 1:numFrame
  frameModelOptCell{iFrame}.delayMode = genOpt.wave.delayMode;
  frameModelOptCell{iFrame}.delayRefMode = genOpt.wave.delayRefMode;
  frameModelOptCell{iFrame}.carrierPhaseMode = genOpt.wave.carrierPhaseMode;
  frameModelOptCell{iFrame}.outputLengthMode = genOpt.wave.outputLengthMode;
  frameModelOptCell{iFrame}.obsStartTime = genOpt.wave.obsStartTime;
  frameModelOptCell{iFrame}.filterHalfLen = genOpt.wave.filterHalfLen;
  frameModelOptCell{iFrame}.windowType = genOpt.wave.windowType;
  frameModelOptCell{iFrame}.useAccessMask = genOpt.wave.useAccessMask;
end
end


function [txSigCell, txShared] = localParseTxSig(txSig, numFrame, numUser)
%LOCALPARSETXSIG Parse shared / frame-wise tx waveform input.

if iscell(txSig)
  if numel(txSig) ~= numFrame
    error('genMultiFrameSnapshots:InvalidTxSigCellSize', ...
      'When txSig is a cell array, it must contain exactly one entry per frame.');
  end

  txSigCell = reshape(txSig, 1, []);
  txShared = false;
else
  txSigCell = repmat({txSig}, 1, numFrame);
  txShared = true;
end

for iFrame = 1:numFrame
  currentTxSig = txSigCell{iFrame};

  if ~isnumeric(currentTxSig) || ~ismatrix(currentTxSig) || isempty(currentTxSig)
    error('genMultiFrameSnapshots:InvalidTxSig', ...
      'Each txSig frame must be a non-empty 2D numeric matrix.');
  end

  if size(currentTxSig, 1) ~= numUser
    error('genMultiFrameSnapshots:TxSigUserSizeMismatch', ...
      'Each txSig frame must have size Nu x T, where Nu matches sceneSeq.numUser.');
  end

  if any(~isfinite(real(currentTxSig(:)))) || any(~isfinite(imag(currentTxSig(:))))
    error('genMultiFrameSnapshots:InvalidTxSigValue', ...
      'Each txSig frame must contain finite values.');
  end
end
end


function frameCell = localExpandFrameInput(inputArg, numFrame, argName)
%LOCALEXPANDFRAMEINPUT Expand shared input into a 1xNf cell array.

if iscell(inputArg)
  if numel(inputArg) ~= numFrame
    error('genMultiFrameSnapshots:FrameInputCellSizeMismatch', ...
