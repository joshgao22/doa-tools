function [periodicFixture, subsetFixtureCell, timing] = buildDynamicRepeatFixtures( ...
  sceneSeqMaster, linkParamCellMaster, rxSigCellMaster, masterOffsetIdx, periodicOffsetIdx, ...
  subsetOffsetCell, subsetLabelList, numSubsetRandomTrial, taskSeed, ...
  gridSize, searchRange, E, wavelen, sampleRate, fdRangeDefault, fdRateRangeDefault, parallelOpt)
%BUILDDYNAMICREPEATFIXTURES Build one periodic fixture and one subset-fixture bank.
% This helper keeps the dev/perf entry scripts light by centralizing the
% repeat-level periodic fixture and the default subset candidates. Curated
% subsets are always constructed explicitly. Random subsets are optional and
% are appended afterwards without relying on any deterministic-first label
% convention.

arguments
  sceneSeqMaster (1, 1) struct
  linkParamCellMaster (1, :) cell
  rxSigCellMaster (1, :) cell
  masterOffsetIdx (1, :) double
  periodicOffsetIdx (1, :) double
  subsetOffsetCell (1, :) cell
  subsetLabelList (:, 1) string
  numSubsetRandomTrial (1, 1) double {mustBeNonnegative, mustBeInteger}
  taskSeed (1, 1) double
  gridSize (1, 2) double
  searchRange (2, 2) double
  E (1, 1)
  wavelen (1, 1) double {mustBePositive}
  sampleRate (1, 1) double {mustBePositive}
  fdRangeDefault (1, 2) double
  fdRateRangeDefault (1, 2) double
  parallelOpt (1, 1) struct = struct()
end

periodicTimer = tic;
periodicFixture = buildDynamicFrameSubsetFixture( ...
  sceneSeqMaster, linkParamCellMaster, rxSigCellMaster, masterOffsetIdx, periodicOffsetIdx, ...
  gridSize, searchRange, E, wavelen, sampleRate, fdRangeDefault, fdRateRangeDefault);
periodicFixtureSec = toc(periodicTimer);

subsetTimer = tic;
subsetFixtureCell = buildDynamicSubsetFixturesByLabel( ...
  sceneSeqMaster, linkParamCellMaster, rxSigCellMaster, masterOffsetIdx, ...
  subsetOffsetCell, subsetLabelList, numSubsetRandomTrial, taskSeed, ...
  localResolveRandomBaseOffset(subsetOffsetCell), gridSize, searchRange, E, ...
  wavelen, sampleRate, fdRangeDefault, fdRateRangeDefault, parallelOpt);
subsetFixtureSec = toc(subsetTimer);

periodicFixture.subsetBuildContext = struct( ...
  'sceneSeqMaster', sceneSeqMaster, ...
  'linkParamCellMaster', {linkParamCellMaster}, ...
  'rxSigCellMaster', {rxSigCellMaster}, ...
  'masterOffsetIdx', reshape(masterOffsetIdx, 1, []), ...
  'subsetOffsetCell', {subsetOffsetCell}, ...
  'subsetLabelList', reshape(string(subsetLabelList), [], 1), ...
  'randomBaseOffsetIdx', localResolveRandomBaseOffset(subsetOffsetCell), ...
  'taskSeed', taskSeed, ...
  'gridSize', gridSize, ...
  'searchRange', searchRange, ...
  'E', E, ...
  'wavelen', wavelen, ...
  'sampleRate', sampleRate, ...
  'fdRangeDefault', fdRangeDefault, ...
  'fdRateRangeDefault', fdRateRangeDefault, ...
  'parallelOpt', parallelOpt);

timing = struct();
timing.buildPeriodicFixtureSec = periodicFixtureSec;
timing.buildSubsetFixtureSec = subsetFixtureSec;
end


function randomBaseOffsetIdx = localResolveRandomBaseOffset(subsetOffsetCell)
%LOCALRESOLVERANDOMBASEOFFSET Return one base schedule for random subsets.

randomBaseOffsetIdx = [0, 1];
if isempty(subsetOffsetCell)
  return;
end
randomBaseOffsetIdx = reshape(subsetOffsetCell{1}, 1, []);
end
